n = input('digite algo')
print(n.isnumeric())# para perguntar se é numero
print(n.isalpha())# para perguntar se é letra
print(n.isalnum())# para perguntar se é letra ou numero
print(n.isupper())# para perguntar se é só com letras maiusculas
print(n.isdecimal())# para verificar se é decimal
print(n.isdigit())# para verificar se é digito
print(n.islower())# para verificar se é apenas letras minusculas
print(n.isidentifier())# para verificar se é um identificador valido
print(n.isspace())# para verificar se tem um espaço em branco
